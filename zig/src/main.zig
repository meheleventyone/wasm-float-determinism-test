// eyeballed torque curve for a 1969 Ford Cortina 1600 GT
const torqueCurveRPM = [_]f64{ 1000, 2000, 3000, 4000, 5000, 6000 };
const torqueCurveTorqueNm = [_]f64{ 60, 105, 120, 124, 112, 87 };

const rpmLimit: f64 = 5900;
const flywheelIntertia: f64 = 0.5 * 10 * 0.1 * 0.1; // 10kg flywheel with a 0.1m radius using a thin disc as the model 
const frictionTorque: f64 = 20; // lame constant friction
const tickTime: f64 = 0.01667; // 60hz tick rate
const revLimitUpper: f64 = rpmToAV(5900);
const revLimitLower: f64 = rpmToAV(5500);

fn avToRPM(av: f64) f64 {
    return (av * 60) / (2 * math.pi);
}

fn rpmToAV(rpm: f64) f64 {
    return (rpm / 60) * (2 * math.pi);
}

fn getTorqueValue(rpm: f64) f64 {
    var upper: usize = 0;
    for (torqueCurveRPM, 0..) |value, i| {
        if (value < rpm) {
            continue;
        }

        upper = i;
        break;
    }

    const percIn: f64 = 1 - ((torqueCurveRPM[upper] - rpm) / 1000);
    const torqueUpper = torqueCurveTorqueNm[upper];
    const torqueLower = if (upper > 0) torqueCurveTorqueNm[upper - 1] else 0;
    return torqueLower + ((torqueUpper - torqueLower) * percIn);
}

export fn start() [*]f64 {
    var engineAngularVelocity: f64 = rpmToAV(1000); // start at 1000 rpm but work in rad/s
    var revLimiterEngaged = false;
    const inverseInteria = 1 / flywheelIntertia; // ordinarily we'd just keep this but lets calculate it for more room for things to go wrong

    for (input, &output) |throttle, *out| {
        const limitedThrottle = if (revLimiterEngaged) 0 else throttle;

        // get torque for current engine rpm
        var torque = getTorqueValue(avToRPM(engineAngularVelocity));
        torque *= limitedThrottle;
        torque -= frictionTorque;

        const acceleration = torque * inverseInteria;
        engineAngularVelocity += acceleration * tickTime;
            
        if (engineAngularVelocity > revLimitUpper) {
            revLimiterEngaged = true;
        } else if (engineAngularVelocity < revLimitLower) {
            revLimiterEngaged = false;
        }

        out.* = engineAngularVelocity;
    }
    return &output;
}
